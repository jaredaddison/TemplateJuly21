	<p>
		Triage is the process by which patients are rapidly prioritized according to
		the severity of their complaints. As vets we often trust the initial triage
		process of our patients to nurses or receptionists and it is important to
		ensure that they are well trained in how to handle this. The following
		provides some key points and an overall framework for the provision of phone
		advice and initial patient triage that can be incorporated into your nursing
		team.
	</p>

	<p>
		<em><strong>Phone Advice</strong></em>
	</p>

	<p>Key Points:</p>

	<ul>
		<li>You cannot adequately triage or assess a patient on the phone.</li>
		<li>The phone is useful for collecting and providing information.</li>
		<li>
			The best advice / recommendation that you can give is for the patient to be
			assessed by a vet.
		</li>
		<li>Poor phone advice can result in patient mortality.</li>
	</ul>

	<p>
		As part of their job veterinary nurses are often required to talk to clients
		on the phone about their pet’s condition or current problem(s). Quite often
		nurses are asked to provide an assessment of a patient’s condition and give
		guidance as to how best to manage that patient. While this is a very common
		practice performed in almost every vet clinic it is also fraught with risks
		and care must be taken with any advice provided over the phone.
	</p>

	<p>
		The major flaw with providing phone advice is that the accuracy of the
		information you receive is highly unreliable. You are usually given an
		assessment of a patient’s problem from an owner who is both emotionally
		attached to their pet and uneducated with respect to a medical assessment.
		This does not form a good basis to be able to effectively assess a patient or
		provide advice on what the problem could be or the potential severity. It is
		also important to realize that advice given out over the phone is seen as
		being ‘veterinary advice’ and is representative of the entire clinic that you
		work for. It is not infrequent that a recommendation provided by a vet in a
		consult room will be challenged because of advice provided over the phone. The
		following is an example of a real case that illustrates this point;
	</p>

	<p>
		A client calls the clinic to say that a queen has just had 3 kittens and is
		breathing a little faster than usual but otherwise seems fine. She is asked
		about previous health issues and according to the owner she is otherwise in
		complete health and prior to having the kittens was absolutely fine. The owner
		believes it is just stress but “just wanted to make sure she would be fine”.
	</p>

	<p>
		Based on the assessment provided by the owner she is advised that there is a
		low level of concern, to monitor her for deterioration and to call back if her
		breathing gets any worse. A few hours later the client presents the cat to the
		clinic for assessment as her breathing continued to deteriorate and as she is
		carried into the clinic she arrests. CPR is not successful at resuscitating
		her.
	</p>

	<p>
		If the client had indicated over the phone that the cat was dyspneic and had a
		heart murmur then the phone advice would likely have been different, and they
		would have been recommended to present immediately for assessment. The take
		home message is that you cannot adequately triage or assess a patient on the
		phone and care must be taken when trying to give advice.
	</p>

	<p>
		With this in mind it is important to ascertain the true value of phone triage
		and how it is most effectively utilized in clinical practice. There are two
		general guidelines that govern effective phone advice. The first is that phone
		calls are best employed for the purpose of gathering and providing information
		as it relates to a particular patient. In gathering information it will give
		you a better ability to appropriately handle the phone call and either give
		the correct information or provide the best advice. The kind of information
		that you need to gather includes but is not limited to;
	</p>

	<ul>
		<li>
			Signalment:

			<ul>
				<li>Age, breed, sex, approximate weight</li>
			</ul>
		</li>
		<li>
			Description of Problem:

			<ul>
				<li>What is the owner’s description of the problem?</li>
				<li>A: Airway and ambulation</li>
				<li>B: Breathing and bleeding.</li>
				<li>C: Circulation and consciousness.</li>
			</ul>
		</li>
		<li>
			Location:

			<ul>
				<li>How far from the clinic.</li>
			</ul>
		</li>
		<li>
			Contact Details:

			<ul>
				<li>Ensure you have a return phone number.</li>
			</ul>
		</li>
	</ul>

	<p>
		The above information will not only help you in being able to correctly
		identify the owner’s concern but it will given you invaluable information in
		the event of an emergency. One key to appropriately managing emergency
		situations is preparedness and having the above information will allow your
		team to anticipate when the patient will arrive, the state it will arrive in
		and allow for appropriate organization beforehand. For example you would
		prepare differently for a 5kg patient who was having a seizure and expected to
		arrive within 5minutes than you would for a 60kg patient who had a single
		seizure 1h ago and was expected to present in a further 2h time.
	</p>

	<p>
		With regards to giving information to clients it becomes a little bit more
		complicated. Information that is provided over the phone to clients should be
		general in nature and in most situations should not discourage them from
		seeking medical attention for their pet. Appropriate information to provide
		usually relates to the potential toxicity of certain items such as the amount
		of chocolate that can be consumed before it is a concern or the potential
		toxicity of an ingested medication etc.
	</p>

	<p>
		In many circumstances information such as this may need to be provided by a
		vet, therefore either recommending a consultation or checking with a vet
		before providing information would be most appropriate.
	</p>

	<p>
		This leads onto what advice or recommendations should be given over the phone.
		The simple answer is that there is only one appropriate recommendation and
		that is to have a veterinary consultation to discuss their concerns and to
		have their pet assessed by a vet. By following this rule it mitigates the
		risks of inappropriately assessing a patient over the phone. It would be
		better for 5 patients to be assessed who are fine than to have 1 patient not
		assessed who is critically ill. It is a general consideration that if the
		owner is concerned enough to call about their pet and raise concern then that
		concern alone warrants a discussion and examination with a vet. The only
		question then remains is how urgent veterinary assessment is and how strongly
		to push that recommendation; this is where being able to gather appropriate
		information is key.
	</p>

	<p>
		We are going to discuss the “ABC – 123” approach to patient triage and this
		holds true for triaging a patient on the phone and forms the basis for the
		information that you need to gather.
	</p>

	<p>
		Remember that the initial phone call with a client is their first point of
		contact with the clinic and often leaves a lasting impression with regards to
		the treatment of their pet. By gaining good information, providing appropriate
		and factual information and recommending veterinary assessment to discuss
		their concerns you are in a position to handle virtually any phone call from a
		distressed or emotional owner.
	</p>

	<p>
		<em><strong>Initial Patient Exam</strong></em>
	</p>

	<p>Key Points:</p>

	<ul>
		<li>Know your normals</li>
		<li>ABC2 - 123</li>
		<li>Primary Survey</li>
	</ul>

	<p>
		Once you have recommended that a patient is presented to the clinic for a
		veterinary assessment it is common that you will be the first person to see
		that patient on its arrival. Often there is a lag time between the patient
		arriving and being assessed by a vet and it is during this time that learning
		how to triage a patient is of critical importance, especially in an emergency
		setting. A triage should take no longer than 30 seconds to complete and as we
		have already established it is designed to be a rapid way to identify patients
		of high risk of deterioration in order to prioritise their care.
	</p>

	<p>
		The foundation of being able to recognize abnormalities in patients is to
		become familiar with what is normal and the best way to do this is to follow
		the same process in assessing every patient that walks into the clinic. To
		make this easier there is a pneumonic that can be used to help with patient
		triage and that is “ABC – 123”
	</p>

	<p>
		<em><strong>ABC of Triage</strong></em>
	</p>

	<ul>
		<li>
			A:

			<ul>
				<li>Airway: Does the patient have a patent airway?</li>
				<li>
					Ambulation: Is the patient walking or has it been carried into the clinic?
				</li>
			</ul>
		</li>
		<li>
			B:

			<ul>
				<li>Breathing: Is the patient cyanotic, tachypneic or dyspneic?</li>
				<li>
					Bleeding: Is the patient bleeding excessively or is there evidence of open
					wounds?
				</li>
			</ul>
		</li>
		<li>
			C:

			<ul>
				<li>
					Circulation: Is the patient in shock or have evidence of poor perfusion?
				</li>
				<li>Consciousness: What is the patient’s awareness and mental state?</li>
			</ul>
		</li>
	</ul>

	<p>
		<em><strong>Triage Levels</strong></em>
	</p>

	<ul>
		<li>
			Triage Level 1:

			<ul>
				<li>These patients require immediate treatment or risk death.</li>
				<li>
					Examples include: cardiac and respiratory arrest, respiratory distress,
					active seizures, GDV, anaphylaxis, severe snail pellets, snakebite,
					excessive bleeding, open fractures.
				</li>
			</ul>
		</li>
		<li>
			Triage Level 2:

			<ul>
				<li>
					These patients are a priority due to unstable or painful conditions and
					should be seen before level 3 patients.
				</li>
				<li>
					Examples include: closed fractures, dystocia, pallor, male cat straining to
					urinate.
				</li>
			</ul>
		</li>
		<li>
			Triage Level 3:

			<ul>
				<li>These patients are non-urgent and can be seen in arrival order.</li>
				<li>
					Examples include: vaccinations, skin conditions, chronic problems, sore
					ears.
				</li>
			</ul>
		</li>
	</ul>

	<p>
		<em><strong>Primary Survey</strong></em>
	</p>

	<p>
		Being able to rapidly assess a patient and allocate them into a triage level
		via the ABC – 123 system equate to what is called a ‘Primary Survey.’ The way
		to perform a primary survey is described below in steps;
	</p>

	<ul>
		<li>
			Step 1: Arrival – Assess the patient visually as it presents to the clinic
			while talking to the client.

			<ul>
				<li>
					A:

					<ul>
						<li>
							Any evidence or loud airway noises i.e. honking, stridor, stertor?
						</li>
						<li>Is the patient ambulatory or is it being carried?</li>
					</ul>
				</li>
				<li>
					B:

					<ul>
						<li>Any evidence of rapid or abnormal breathing?</li>
						<li>Any obvious bleeding?</li>
					</ul>
				</li>
				<li>
					C:

					<ul>
						<li>Is the patient alert and interactive?</li>
						<li>Is the patient neurological i.e. seizuring or ataxic?</li>
					</ul>
				</li>
			</ul>
		</li>
		<li>
			Step 2: Physical Exam – Quickly assess the patient (30 seconds max).

			<ul>
				<li>A:</li>
				<li>
					B:

					<ul>
						<li>
							Check gum colour for cyanosis (and at the same time evidence of pallor
							and check CRT)
						</li>
						<li>Take a respiratory rate and observe the effort.</li>
						<li>Auscultate the lungs.</li>
						<li>Assess any wounds.</li>
					</ul>
				</li>
				<li>
					C:

					<ul>
						<li>
							Check gum colour for pallor and check CRT (and at the same time for
							cyanosis)
						</li>
						<li>Take a heart rate and check for femoral and dorsal pedal pulse.</li>
						<li>Assess palpebral and menace reflex</li>
					</ul>
				</li>
			</ul>
		</li>
	</ul>
